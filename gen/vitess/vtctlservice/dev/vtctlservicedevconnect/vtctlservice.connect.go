	GetShardRoutingRules(context.Context, *connect_go.Request[dev.GetShardRoutingRulesRequest]) (*connect_go.Response[dev.GetShardRoutingRulesResponse], error)
	// GetSrvKeyspaceNames returns a mapping of cell name to the keyspaces served
	// in that cell.
	GetSrvKeyspaceNames(context.Context, *connect_go.Request[dev.GetSrvKeyspaceNamesRequest]) (*connect_go.Response[dev.GetSrvKeyspaceNamesResponse], error)
	// GetSrvKeyspaces returns the SrvKeyspaces for a keyspace in one or more
	// cells.
	GetSrvKeyspaces(context.Context, *connect_go.Request[dev.GetSrvKeyspacesRequest]) (*connect_go.Response[dev.GetSrvKeyspacesResponse], error)
	// UpdateThrottlerConfig updates the tablet throttler configuration
	UpdateThrottlerConfig(context.Context, *connect_go.Request[dev.UpdateThrottlerConfigRequest]) (*connect_go.Response[dev.UpdateThrottlerConfigResponse], error)
	// GetSrvVSchema returns the SrvVSchema for a cell.
	GetSrvVSchema(context.Context, *connect_go.Request[dev.GetSrvVSchemaRequest]) (*connect_go.Response[dev.GetSrvVSchemaResponse], error)
