	// https://vitess.io/docs/user-guides/configuration-advanced/reparenting/#external-reparenting.
	TabletExternallyReparented(context.Context, *connect_go.Request[dev.TabletExternallyReparentedRequest]) (*connect_go.Response[dev.TabletExternallyReparentedResponse], error)
	// UpdateCellInfo updates the content of a CellInfo with the provided
