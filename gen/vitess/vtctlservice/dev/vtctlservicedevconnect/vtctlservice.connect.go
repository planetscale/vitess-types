	// list.
	RemoveShardCell(context.Context, *connect_go.Request[dev.RemoveShardCellRequest]) (*connect_go.Response[dev.RemoveShardCellResponse], error)
	// ReparentTablet reparents a tablet to the current primary in the shard. This
	// only works if the current replica position matches the last known reparent
	// action.
	ReparentTablet(context.Context, *connect_go.Request[dev.ReparentTabletRequest]) (*connect_go.Response[dev.ReparentTabletResponse], error)
	// RestoreFromBackup stops mysqld for the given tablet and restores a backup.
	RestoreFromBackup(context.Context, *connect_go.Request[dev.RestoreFromBackupRequest]) (*connect_go.ServerStreamForClient[dev.RestoreFromBackupResponse], error)
	// RunHealthCheck runs a healthcheck on the remote tablet.
	RunHealthCheck(context.Context, *connect_go.Request[dev.RunHealthCheckRequest]) (*connect_go.Response[dev.RunHealthCheckResponse], error)
	// SetKeyspaceDurabilityPolicy updates the DurabilityPolicy for a keyspace.
	SetKeyspaceDurabilityPolicy(context.Context, *connect_go.Request[dev.SetKeyspaceDurabilityPolicyRequest]) (*connect_go.Response[dev.SetKeyspaceDurabilityPolicyResponse], error)
