	ShardReplicationRemove(context.Context, *connect_go.Request[dev.ShardReplicationRemoveRequest]) (*connect_go.Response[dev.ShardReplicationRemoveResponse], error)
	// SleepTablet blocks the aciton queue on the specified tablet for the
	// specified duration.
	//
	// This is typically used for testing.
	SleepTablet(context.Context, *connect_go.Request[dev.SleepTabletRequest]) (*connect_go.Response[dev.SleepTabletResponse], error)
	// SourceShardAdd adds the SourceShard record with the provided index. This
	// should be used only as an emergency function.
	//
