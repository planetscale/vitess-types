	GetRoutingRules(context.Context, *connect_go.Request[dev.GetRoutingRulesRequest]) (*connect_go.Response[dev.GetRoutingRulesResponse], error)
	// GetSchema returns the schema for a tablet, or just the schema for the
	// specified tables in that tablet.
	GetSchema(context.Context, *connect_go.Request[dev.GetSchemaRequest]) (*connect_go.Response[dev.GetSchemaResponse], error)
	// GetShard returns information about a shard in the topology.
