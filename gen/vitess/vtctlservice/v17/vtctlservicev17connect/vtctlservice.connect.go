	// GetFullStatus returns the full status of MySQL including the replication information, semi-sync information, GTID information among others
	GetFullStatus(context.Context, *connect.Request[v17.GetFullStatusRequest]) (*connect.Response[v17.GetFullStatusResponse], error)
	// GetKeyspace reads the given keyspace from the topo and returns it.
	GetKeyspace(context.Context, *connect.Request[v17.GetKeyspaceRequest]) (*connect.Response[v17.GetKeyspaceResponse], error)
	// GetKeyspaces returns the keyspace struct of all keyspaces in the topo.
	GetKeyspaces(context.Context, *connect.Request[v17.GetKeyspacesRequest]) (*connect.Response[v17.GetKeyspacesResponse], error)
	// GetPermissions returns the permissions set on the remote tablet.
	GetPermissions(context.Context, *connect.Request[v17.GetPermissionsRequest]) (*connect.Response[v17.GetPermissionsResponse], error)
	// GetRoutingRules returns the VSchema routing rules.
	GetRoutingRules(context.Context, *connect.Request[v17.GetRoutingRulesRequest]) (*connect.Response[v17.GetRoutingRulesResponse], error)
	// GetSchema returns the schema for a tablet, or just the schema for the
	// specified tables in that tablet.
	GetSchema(context.Context, *connect.Request[v17.GetSchemaRequest]) (*connect.Response[v17.GetSchemaResponse], error)
	// GetShard returns information about a shard in the topology.
	GetShard(context.Context, *connect.Request[v17.GetShardRequest]) (*connect.Response[v17.GetShardResponse], error)
	// GetShardRoutingRules returns the VSchema shard routing rules.
	GetShardRoutingRules(context.Context, *connect.Request[v17.GetShardRoutingRulesRequest]) (*connect.Response[v17.GetShardRoutingRulesResponse], error)
	// GetSrvKeyspaceNames returns a mapping of cell name to the keyspaces served
	// in that cell.
	GetSrvKeyspaceNames(context.Context, *connect.Request[v17.GetSrvKeyspaceNamesRequest]) (*connect.Response[v17.GetSrvKeyspaceNamesResponse], error)
	// GetSrvKeyspaces returns the SrvKeyspaces for a keyspace in one or more
	// cells.
	GetSrvKeyspaces(context.Context, *connect.Request[v17.GetSrvKeyspacesRequest]) (*connect.Response[v17.GetSrvKeyspacesResponse], error)
	// UpdateThrottlerConfig updates the tablet throttler configuration
	UpdateThrottlerConfig(context.Context, *connect.Request[v17.UpdateThrottlerConfigRequest]) (*connect.Response[v17.UpdateThrottlerConfigResponse], error)
	// GetSrvVSchema returns the SrvVSchema for a cell.
	GetSrvVSchema(context.Context, *connect.Request[v17.GetSrvVSchemaRequest]) (*connect.Response[v17.GetSrvVSchemaResponse], error)
	// GetSrvVSchemas returns a mapping from cell name to SrvVSchema for all cells,
