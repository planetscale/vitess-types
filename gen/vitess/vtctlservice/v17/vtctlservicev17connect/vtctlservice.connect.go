	RemoveKeyspaceCell(context.Context, *connect.Request[v17.RemoveKeyspaceCellRequest]) (*connect.Response[v17.RemoveKeyspaceCellResponse], error)
	// RemoveShardCell removes the specified cell from the specified shard's Cells
	// list.
	RemoveShardCell(context.Context, *connect.Request[v17.RemoveShardCellRequest]) (*connect.Response[v17.RemoveShardCellResponse], error)
