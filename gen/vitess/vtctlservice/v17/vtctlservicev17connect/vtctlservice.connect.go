	GetVersion(context.Context, *connect.Request[v17.GetVersionRequest]) (*connect.Response[v17.GetVersionResponse], error)
	// GetVSchema returns the vschema for a keyspace.
	GetVSchema(context.Context, *connect.Request[v17.GetVSchemaRequest]) (*connect.Response[v17.GetVSchemaResponse], error)
	// GetWorkflows returns a list of workflows for the given keyspace.
	GetWorkflows(context.Context, *connect.Request[v17.GetWorkflowsRequest]) (*connect.Response[v17.GetWorkflowsResponse], error)
