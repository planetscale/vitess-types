	// PlannedReparentShard or EmergencyReparentShard should be used in those
	// cases instead.
	InitShardPrimary(context.Context, *connect.Request[v17.InitShardPrimaryRequest]) (*connect.Response[v17.InitShardPrimaryResponse], error)
