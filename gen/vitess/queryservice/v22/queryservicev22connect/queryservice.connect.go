// QueryHandler is an implementation of the vitess.queryservice.v22.Query service.
type QueryHandler interface {
	// Execute executes the specified SQL query (might be in a
	// transaction context, if Query.transaction_id is set).
	Execute(context.Context, *connect.Request[v22.ExecuteRequest]) (*connect.Response[v22.ExecuteResponse], error)
	// StreamExecute executes a streaming query. Use this method if the
	// query returns a large number of rows. The first QueryResult will
	// contain the Fields, subsequent QueryResult messages will contain
	// the rows.
	StreamExecute(context.Context, *connect.Request[v22.StreamExecuteRequest], *connect.ServerStream[v22.StreamExecuteResponse]) error
	// Begin a transaction.
	Begin(context.Context, *connect.Request[v22.BeginRequest]) (*connect.Response[v22.BeginResponse], error)
	// Commit a transaction.
	Commit(context.Context, *connect.Request[v22.CommitRequest]) (*connect.Response[v22.CommitResponse], error)
	// Rollback a transaction.
	Rollback(context.Context, *connect.Request[v22.RollbackRequest]) (*connect.Response[v22.RollbackResponse], error)
	// Prepare preares a transaction.
	Prepare(context.Context, *connect.Request[v22.PrepareRequest]) (*connect.Response[v22.PrepareResponse], error)
	// CommitPrepared commits a prepared transaction.
	CommitPrepared(context.Context, *connect.Request[v22.CommitPreparedRequest]) (*connect.Response[v22.CommitPreparedResponse], error)
	// RollbackPrepared rolls back a prepared transaction.
	RollbackPrepared(context.Context, *connect.Request[v22.RollbackPreparedRequest]) (*connect.Response[v22.RollbackPreparedResponse], error)
	// CreateTransaction creates the metadata for a 2pc transaction.
	CreateTransaction(context.Context, *connect.Request[v22.CreateTransactionRequest]) (*connect.Response[v22.CreateTransactionResponse], error)
	// StartCommit initiates a commit for a 2pc transaction.
	StartCommit(context.Context, *connect.Request[v22.StartCommitRequest]) (*connect.Response[v22.StartCommitResponse], error)
	// SetRollback marks the 2pc transaction for rollback.
	SetRollback(context.Context, *connect.Request[v22.SetRollbackRequest]) (*connect.Response[v22.SetRollbackResponse], error)
	// ConcludeTransaction marks the 2pc transaction as resolved.
	ConcludeTransaction(context.Context, *connect.Request[v22.ConcludeTransactionRequest]) (*connect.Response[v22.ConcludeTransactionResponse], error)
	// ReadTransaction returns the 2pc transaction info.
	ReadTransaction(context.Context, *connect.Request[v22.ReadTransactionRequest]) (*connect.Response[v22.ReadTransactionResponse], error)
	// UnresolvedTransactions returns the 2pc transaction info.
	UnresolvedTransactions(context.Context, *connect.Request[v22.UnresolvedTransactionsRequest]) (*connect.Response[v22.UnresolvedTransactionsResponse], error)
	// BeginExecute executes a begin and the specified SQL query.
	BeginExecute(context.Context, *connect.Request[v22.BeginExecuteRequest]) (*connect.Response[v22.BeginExecuteResponse], error)
	// BeginStreamExecute executes a begin and the specified SQL query.
	BeginStreamExecute(context.Context, *connect.Request[v22.BeginStreamExecuteRequest], *connect.ServerStream[v22.BeginStreamExecuteResponse]) error
	// MessageStream streams messages from a message table.
	MessageStream(context.Context, *connect.Request[v22.MessageStreamRequest], *connect.ServerStream[v22.MessageStreamResponse]) error
	// MessageAck acks messages for a table.
	MessageAck(context.Context, *connect.Request[v22.MessageAckRequest]) (*connect.Response[v22.MessageAckResponse], error)
	// ReserveExecute executes a query on a reserved connection
	ReserveExecute(context.Context, *connect.Request[v22.ReserveExecuteRequest]) (*connect.Response[v22.ReserveExecuteResponse], error)
	// ReserveBeginExecute starts a transaction and executes a query in the transaction on a reserved connection
	ReserveBeginExecute(context.Context, *connect.Request[v22.ReserveBeginExecuteRequest]) (*connect.Response[v22.ReserveBeginExecuteResponse], error)
	// ReserveStreamExecute executes a streaming query on a reserved connection
	ReserveStreamExecute(context.Context, *connect.Request[v22.ReserveStreamExecuteRequest], *connect.ServerStream[v22.ReserveStreamExecuteResponse]) error
	// ReserveBeginStreamExecute starts a transaction and executes a streaming query in the transaction on a reserved connection
	ReserveBeginStreamExecute(context.Context, *connect.Request[v22.ReserveBeginStreamExecuteRequest], *connect.ServerStream[v22.ReserveBeginStreamExecuteResponse]) error
	// Release releases the connection
	Release(context.Context, *connect.Request[v22.ReleaseRequest]) (*connect.Response[v22.ReleaseResponse], error)
	// StreamHealth runs a streaming RPC to the tablet, that returns the
	// current health of the tablet on a regular basis.
	StreamHealth(context.Context, *connect.Request[v22.StreamHealthRequest], *connect.ServerStream[v22.StreamHealthResponse]) error
	// VStream streams vreplication events.
	VStream(context.Context, *connect.Request[v221.VStreamRequest], *connect.ServerStream[v221.VStreamResponse]) error
	// VStreamRows streams rows from the specified starting point.
	VStreamRows(context.Context, *connect.Request[v221.VStreamRowsRequest], *connect.ServerStream[v221.VStreamRowsResponse]) error
	// VStreamTables streams rows from the specified starting point.
	VStreamTables(context.Context, *connect.Request[v221.VStreamTablesRequest], *connect.ServerStream[v221.VStreamTablesResponse]) error
	// VStreamResults streams results along with the gtid of the snapshot.
	VStreamResults(context.Context, *connect.Request[v221.VStreamResultsRequest], *connect.ServerStream[v221.VStreamResultsResponse]) error
	// GetSchema returns the schema information.
	GetSchema(context.Context, *connect.Request[v22.GetSchemaRequest], *connect.ServerStream[v22.GetSchemaResponse]) error
}

