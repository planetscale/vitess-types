// QueryHandler is an implementation of the vitess.queryservice.v16.Query service.
type QueryHandler interface {
	// Execute executes the specified SQL query (might be in a
	// transaction context, if Query.transaction_id is set).
	Execute(context.Context, *connect_go.Request[v16.ExecuteRequest]) (*connect_go.Response[v16.ExecuteResponse], error)
	// StreamExecute executes a streaming query. Use this method if the
	// query returns a large number of rows. The first QueryResult will
	// contain the Fields, subsequent QueryResult messages will contain
	// the rows.
	StreamExecute(context.Context, *connect_go.Request[v16.StreamExecuteRequest], *connect_go.ServerStream[v16.StreamExecuteResponse]) error
	// Begin a transaction.
	Begin(context.Context, *connect_go.Request[v16.BeginRequest]) (*connect_go.Response[v16.BeginResponse], error)
	// Commit a transaction.
	Commit(context.Context, *connect_go.Request[v16.CommitRequest]) (*connect_go.Response[v16.CommitResponse], error)
	// Rollback a transaction.
	Rollback(context.Context, *connect_go.Request[v16.RollbackRequest]) (*connect_go.Response[v16.RollbackResponse], error)
	// Prepare preares a transaction.
	Prepare(context.Context, *connect_go.Request[v16.PrepareRequest]) (*connect_go.Response[v16.PrepareResponse], error)
	// CommitPrepared commits a prepared transaction.
	CommitPrepared(context.Context, *connect_go.Request[v16.CommitPreparedRequest]) (*connect_go.Response[v16.CommitPreparedResponse], error)
	// RollbackPrepared rolls back a prepared transaction.
	RollbackPrepared(context.Context, *connect_go.Request[v16.RollbackPreparedRequest]) (*connect_go.Response[v16.RollbackPreparedResponse], error)
	// CreateTransaction creates the metadata for a 2pc transaction.
	CreateTransaction(context.Context, *connect_go.Request[v16.CreateTransactionRequest]) (*connect_go.Response[v16.CreateTransactionResponse], error)
	// StartCommit initiates a commit for a 2pc transaction.
	StartCommit(context.Context, *connect_go.Request[v16.StartCommitRequest]) (*connect_go.Response[v16.StartCommitResponse], error)
	// SetRollback marks the 2pc transaction for rollback.
	SetRollback(context.Context, *connect_go.Request[v16.SetRollbackRequest]) (*connect_go.Response[v16.SetRollbackResponse], error)
	// ConcludeTransaction marks the 2pc transaction as resolved.
	ConcludeTransaction(context.Context, *connect_go.Request[v16.ConcludeTransactionRequest]) (*connect_go.Response[v16.ConcludeTransactionResponse], error)
	// ReadTransaction returns the 2pc transaction info.
	ReadTransaction(context.Context, *connect_go.Request[v16.ReadTransactionRequest]) (*connect_go.Response[v16.ReadTransactionResponse], error)
	// BeginExecute executes a begin and the specified SQL query.
	BeginExecute(context.Context, *connect_go.Request[v16.BeginExecuteRequest]) (*connect_go.Response[v16.BeginExecuteResponse], error)
	// BeginStreamExecute executes a begin and the specified SQL query.
	BeginStreamExecute(context.Context, *connect_go.Request[v16.BeginStreamExecuteRequest], *connect_go.ServerStream[v16.BeginStreamExecuteResponse]) error
	// MessageStream streams messages from a message table.
	MessageStream(context.Context, *connect_go.Request[v16.MessageStreamRequest], *connect_go.ServerStream[v16.MessageStreamResponse]) error
	// MessageAck acks messages for a table.
	MessageAck(context.Context, *connect_go.Request[v16.MessageAckRequest]) (*connect_go.Response[v16.MessageAckResponse], error)
	// ReserveExecute executes a query on a reserved connection
	ReserveExecute(context.Context, *connect_go.Request[v16.ReserveExecuteRequest]) (*connect_go.Response[v16.ReserveExecuteResponse], error)
	// ReserveBeginExecute starts a transaction and executes a query in the transaction on a reserved connection
	ReserveBeginExecute(context.Context, *connect_go.Request[v16.ReserveBeginExecuteRequest]) (*connect_go.Response[v16.ReserveBeginExecuteResponse], error)
	// ReserveStreamExecute executes a streaming query on a reserved connection
	ReserveStreamExecute(context.Context, *connect_go.Request[v16.ReserveStreamExecuteRequest], *connect_go.ServerStream[v16.ReserveStreamExecuteResponse]) error
	// ReserveBeginStreamExecute starts a transaction and executes a streaming query in the transaction on a reserved connection
	ReserveBeginStreamExecute(context.Context, *connect_go.Request[v16.ReserveBeginStreamExecuteRequest], *connect_go.ServerStream[v16.ReserveBeginStreamExecuteResponse]) error
	// Release releases the connection
	Release(context.Context, *connect_go.Request[v16.ReleaseRequest]) (*connect_go.Response[v16.ReleaseResponse], error)
	// StreamHealth runs a streaming RPC to the tablet, that returns the
	// current health of the tablet on a regular basis.
	StreamHealth(context.Context, *connect_go.Request[v16.StreamHealthRequest], *connect_go.ServerStream[v16.StreamHealthResponse]) error
	// VStream streams vreplication events.
	VStream(context.Context, *connect_go.Request[v161.VStreamRequest], *connect_go.ServerStream[v161.VStreamResponse]) error
	// VStreamRows streams rows from the specified starting point.
	VStreamRows(context.Context, *connect_go.Request[v161.VStreamRowsRequest], *connect_go.ServerStream[v161.VStreamRowsResponse]) error
	// VStreamResults streams results along with the gtid of the snapshot.
	VStreamResults(context.Context, *connect_go.Request[v161.VStreamResultsRequest], *connect_go.ServerStream[v161.VStreamResultsResponse]) error
	// GetSchema returns the schema information.
	GetSchema(context.Context, *connect_go.Request[v16.GetSchemaRequest], *connect_go.ServerStream[v16.GetSchemaResponse]) error
}

