// QueryHandler is an implementation of the vitess.queryservice.v15.Query service.
type QueryHandler interface {
	// Execute executes the specified SQL query (might be in a
	// transaction context, if Query.transaction_id is set).
	Execute(context.Context, *connect_go.Request[v15.ExecuteRequest]) (*connect_go.Response[v15.ExecuteResponse], error)
	// StreamExecute executes a streaming query. Use this method if the
	// query returns a large number of rows. The first QueryResult will
	// contain the Fields, subsequent QueryResult messages will contain
	// the rows.
	StreamExecute(context.Context, *connect_go.Request[v15.StreamExecuteRequest], *connect_go.ServerStream[v15.StreamExecuteResponse]) error
	// Begin a transaction.
	Begin(context.Context, *connect_go.Request[v15.BeginRequest]) (*connect_go.Response[v15.BeginResponse], error)
	// Commit a transaction.
	Commit(context.Context, *connect_go.Request[v15.CommitRequest]) (*connect_go.Response[v15.CommitResponse], error)
	// Rollback a transaction.
	Rollback(context.Context, *connect_go.Request[v15.RollbackRequest]) (*connect_go.Response[v15.RollbackResponse], error)
	// Prepare preares a transaction.
	Prepare(context.Context, *connect_go.Request[v15.PrepareRequest]) (*connect_go.Response[v15.PrepareResponse], error)
	// CommitPrepared commits a prepared transaction.
	CommitPrepared(context.Context, *connect_go.Request[v15.CommitPreparedRequest]) (*connect_go.Response[v15.CommitPreparedResponse], error)
	// RollbackPrepared rolls back a prepared transaction.
	RollbackPrepared(context.Context, *connect_go.Request[v15.RollbackPreparedRequest]) (*connect_go.Response[v15.RollbackPreparedResponse], error)
	// CreateTransaction creates the metadata for a 2pc transaction.
	CreateTransaction(context.Context, *connect_go.Request[v15.CreateTransactionRequest]) (*connect_go.Response[v15.CreateTransactionResponse], error)
	// StartCommit initiates a commit for a 2pc transaction.
	StartCommit(context.Context, *connect_go.Request[v15.StartCommitRequest]) (*connect_go.Response[v15.StartCommitResponse], error)
	// SetRollback marks the 2pc transaction for rollback.
	SetRollback(context.Context, *connect_go.Request[v15.SetRollbackRequest]) (*connect_go.Response[v15.SetRollbackResponse], error)
	// ConcludeTransaction marks the 2pc transaction as resolved.
	ConcludeTransaction(context.Context, *connect_go.Request[v15.ConcludeTransactionRequest]) (*connect_go.Response[v15.ConcludeTransactionResponse], error)
	// ReadTransaction returns the 2pc transaction info.
	ReadTransaction(context.Context, *connect_go.Request[v15.ReadTransactionRequest]) (*connect_go.Response[v15.ReadTransactionResponse], error)
	// BeginExecute executes a begin and the specified SQL query.
	BeginExecute(context.Context, *connect_go.Request[v15.BeginExecuteRequest]) (*connect_go.Response[v15.BeginExecuteResponse], error)
	// BeginStreamExecute executes a begin and the specified SQL query.
	BeginStreamExecute(context.Context, *connect_go.Request[v15.BeginStreamExecuteRequest], *connect_go.ServerStream[v15.BeginStreamExecuteResponse]) error
	// MessageStream streams messages from a message table.
	MessageStream(context.Context, *connect_go.Request[v15.MessageStreamRequest], *connect_go.ServerStream[v15.MessageStreamResponse]) error
	// MessageAck acks messages for a table.
	MessageAck(context.Context, *connect_go.Request[v15.MessageAckRequest]) (*connect_go.Response[v15.MessageAckResponse], error)
	// ReserveExecute executes a query on a reserved connection
	ReserveExecute(context.Context, *connect_go.Request[v15.ReserveExecuteRequest]) (*connect_go.Response[v15.ReserveExecuteResponse], error)
	// ReserveBeginExecute starts a transaction and executes a query in the transaction on a reserved connection
	ReserveBeginExecute(context.Context, *connect_go.Request[v15.ReserveBeginExecuteRequest]) (*connect_go.Response[v15.ReserveBeginExecuteResponse], error)
	// ReserveStreamExecute executes a streaming query on a reserved connection
	ReserveStreamExecute(context.Context, *connect_go.Request[v15.ReserveStreamExecuteRequest], *connect_go.ServerStream[v15.ReserveStreamExecuteResponse]) error
	// ReserveBeginStreamExecute starts a transaction and executes a streaming query in the transaction on a reserved connection
	ReserveBeginStreamExecute(context.Context, *connect_go.Request[v15.ReserveBeginStreamExecuteRequest], *connect_go.ServerStream[v15.ReserveBeginStreamExecuteResponse]) error
	// Release releases the connection
	Release(context.Context, *connect_go.Request[v15.ReleaseRequest]) (*connect_go.Response[v15.ReleaseResponse], error)
	// StreamHealth runs a streaming RPC to the tablet, that returns the
	// current health of the tablet on a regular basis.
	StreamHealth(context.Context, *connect_go.Request[v15.StreamHealthRequest], *connect_go.ServerStream[v15.StreamHealthResponse]) error
	// VStream streams vreplication events.
	VStream(context.Context, *connect_go.Request[v151.VStreamRequest], *connect_go.ServerStream[v151.VStreamResponse]) error
	// VStreamRows streams rows from the specified starting point.
	VStreamRows(context.Context, *connect_go.Request[v151.VStreamRowsRequest], *connect_go.ServerStream[v151.VStreamRowsResponse]) error
	// VStreamResults streams results along with the gtid of the snapshot.
	VStreamResults(context.Context, *connect_go.Request[v151.VStreamResultsRequest], *connect_go.ServerStream[v151.VStreamResultsResponse]) error
}

