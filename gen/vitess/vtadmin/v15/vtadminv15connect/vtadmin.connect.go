// VTAdminHandler is an implementation of the vitess.vtadmin.v15.VTAdmin service.
type VTAdminHandler interface {
	// CreateKeyspace creates a new keyspace in the given cluster.
	CreateKeyspace(context.Context, *connect_go.Request[v15.CreateKeyspaceRequest]) (*connect_go.Response[v15.CreateKeyspaceResponse], error)
	// CreateShard creates a new shard in the given cluster and keyspace.
	CreateShard(context.Context, *connect_go.Request[v15.CreateShardRequest]) (*connect_go.Response[v151.CreateShardResponse], error)
	// DeleteKeyspace deletes a keyspace in the given cluster.
	DeleteKeyspace(context.Context, *connect_go.Request[v15.DeleteKeyspaceRequest]) (*connect_go.Response[v151.DeleteKeyspaceResponse], error)
	// DeleteShard deletes one or more shards in the given cluster and keyspace.
	DeleteShards(context.Context, *connect_go.Request[v15.DeleteShardsRequest]) (*connect_go.Response[v151.DeleteShardsResponse], error)
	// DeleteTablet deletes a tablet from the topology
	DeleteTablet(context.Context, *connect_go.Request[v15.DeleteTabletRequest]) (*connect_go.Response[v15.DeleteTabletResponse], error)
	// EmergencyFailoverShard fails over a shard to a new primary. It assumes
	// the old primary is dead or otherwise not responding.
	EmergencyFailoverShard(context.Context, *connect_go.Request[v15.EmergencyFailoverShardRequest]) (*connect_go.Response[v15.EmergencyFailoverShardResponse], error)
	// FindSchema returns a single Schema that matches the provided table name
	// across all specified clusters IDs. Not specifying a set of cluster IDs
	// causes the search to span all configured clusters.
	//
	// An error occurs if either no table exists across any of the clusters with
	// the specified table name, or if multiple tables exist with that name.
	FindSchema(context.Context, *connect_go.Request[v15.FindSchemaRequest]) (*connect_go.Response[v15.Schema], error)
	// GetBackups returns backups grouped by cluster.
	GetBackups(context.Context, *connect_go.Request[v15.GetBackupsRequest]) (*connect_go.Response[v15.GetBackupsResponse], error)
	// GetCellInfos returns the CellInfo objects for the specified clusters.
	//
	// Callers may optionally restrict the set of CellInfos, or restrict the
	// response to include only cell names.
	GetCellInfos(context.Context, *connect_go.Request[v15.GetCellInfosRequest]) (*connect_go.Response[v15.GetCellInfosResponse], error)
	// GetCellsAliases returns the CellsAliases data for the specified clusters.
	GetCellsAliases(context.Context, *connect_go.Request[v15.GetCellsAliasesRequest]) (*connect_go.Response[v15.GetCellsAliasesResponse], error)
	// GetClusters returns all configured clusters.
	GetClusters(context.Context, *connect_go.Request[v15.GetClustersRequest]) (*connect_go.Response[v15.GetClustersResponse], error)
	// GetFullStatus returns the full status of MySQL including the replication information, semi-sync information, GTID information among others
	GetFullStatus(context.Context, *connect_go.Request[v15.GetFullStatusRequest]) (*connect_go.Response[v151.GetFullStatusResponse], error)
	// GetGates returns all gates across all the specified clusters.
	GetGates(context.Context, *connect_go.Request[v15.GetGatesRequest]) (*connect_go.Response[v15.GetGatesResponse], error)
	// GetKeyspace returns a keyspace by name in the specified cluster.
	GetKeyspace(context.Context, *connect_go.Request[v15.GetKeyspaceRequest]) (*connect_go.Response[v15.Keyspace], error)
	// GetKeyspaces returns all keyspaces across the specified clusters.
	GetKeyspaces(context.Context, *connect_go.Request[v15.GetKeyspacesRequest]) (*connect_go.Response[v15.GetKeyspacesResponse], error)
	// GetSchema returns the schema for the specified (cluster, keyspace, table)
	// tuple.
	GetSchema(context.Context, *connect_go.Request[v15.GetSchemaRequest]) (*connect_go.Response[v15.Schema], error)
	// GetSchemas returns all schemas across the specified clusters.
	GetSchemas(context.Context, *connect_go.Request[v15.GetSchemasRequest]) (*connect_go.Response[v15.GetSchemasResponse], error)
	// GetShardReplicationPositions returns shard replication positions grouped
	// by cluster.
	GetShardReplicationPositions(context.Context, *connect_go.Request[v15.GetShardReplicationPositionsRequest]) (*connect_go.Response[v15.GetShardReplicationPositionsResponse], error)
	// GetSrvVSchema returns the SrvVSchema for the given cluster and cell.
	GetSrvVSchema(context.Context, *connect_go.Request[v15.GetSrvVSchemaRequest]) (*connect_go.Response[v15.SrvVSchema], error)
	// GetSrvVSchemas returns all SrvVSchemas across all (or specified) clusters
	// and cells.
	GetSrvVSchemas(context.Context, *connect_go.Request[v15.GetSrvVSchemasRequest]) (*connect_go.Response[v15.GetSrvVSchemasResponse], error)
	// GetTablet looks up a tablet by hostname across all clusters and returns
	// the result.
	GetTablet(context.Context, *connect_go.Request[v15.GetTabletRequest]) (*connect_go.Response[v15.Tablet], error)
	// GetTablets returns all tablets across all the specified clusters.
	GetTablets(context.Context, *connect_go.Request[v15.GetTabletsRequest]) (*connect_go.Response[v15.GetTabletsResponse], error)
	// GetTopologyPath returns the cell located at the specified path in the topology server.
	GetTopologyPath(context.Context, *connect_go.Request[v15.GetTopologyPathRequest]) (*connect_go.Response[v151.GetTopologyPathResponse], error)
	// GetVSchema returns a VSchema for the specified keyspace in the specified
	// cluster.
	GetVSchema(context.Context, *connect_go.Request[v15.GetVSchemaRequest]) (*connect_go.Response[v15.VSchema], error)
	// GetVSchemas returns the VSchemas for all specified clusters.
	GetVSchemas(context.Context, *connect_go.Request[v15.GetVSchemasRequest]) (*connect_go.Response[v15.GetVSchemasResponse], error)
	// GetVtctlds returns the Vtctlds for all specified clusters.
	GetVtctlds(context.Context, *connect_go.Request[v15.GetVtctldsRequest]) (*connect_go.Response[v15.GetVtctldsResponse], error)
	// GetWorkflow returns a single Workflow for a given cluster, keyspace, and
	// workflow name.
	GetWorkflow(context.Context, *connect_go.Request[v15.GetWorkflowRequest]) (*connect_go.Response[v15.Workflow], error)
	// GetWorkflows returns the Workflows for all specified clusters.
	GetWorkflows(context.Context, *connect_go.Request[v15.GetWorkflowsRequest]) (*connect_go.Response[v15.GetWorkflowsResponse], error)
	// PingTablet checks that the specified tablet is awake and responding to
	// RPCs. This command can be blocked by other in-flight operations.
	PingTablet(context.Context, *connect_go.Request[v15.PingTabletRequest]) (*connect_go.Response[v15.PingTabletResponse], error)
	// PlannedFailoverShard fails over the shard to a new primary, or away from
	// an old primary. Both the old and new primaries must be reachable and
	// running.
	//
	// NOTE: A planned failover will not consider replicas outside the current
	// shard primary's cell as promotion candidates unless NewPrimary is
	// explicitly provided in the request.
	PlannedFailoverShard(context.Context, *connect_go.Request[v15.PlannedFailoverShardRequest]) (*connect_go.Response[v15.PlannedFailoverShardResponse], error)
	// RebuildKeyspaceGraph rebuilds the serving data for a keyspace.
	RebuildKeyspaceGraph(context.Context, *connect_go.Request[v15.RebuildKeyspaceGraphRequest]) (*connect_go.Response[v15.RebuildKeyspaceGraphResponse], error)
	// RefreshState reloads the tablet record on the specified tablet.
	RefreshState(context.Context, *connect_go.Request[v15.RefreshStateRequest]) (*connect_go.Response[v15.RefreshStateResponse], error)
	// RefreshTabletReplicationSource performs a `CHANGE REPLICATION SOURCE TO`
	// on a tablet to replicate from the current primary in the shard.
	RefreshTabletReplicationSource(context.Context, *connect_go.Request[v15.RefreshTabletReplicationSourceRequest]) (*connect_go.Response[v15.RefreshTabletReplicationSourceResponse], error)
	// ReloadSchemas reloads the schema definition across keyspaces, shards, or
	// tablets in one or more clusters, depending on the request fields (see
	// ReloadSchemasRequest for details).
	ReloadSchemas(context.Context, *connect_go.Request[v15.ReloadSchemasRequest]) (*connect_go.Response[v15.ReloadSchemasResponse], error)
	// ReloadSchemaShard reloads the schema on all tablets in a shard. This is done on a best-effort basis.
	ReloadSchemaShard(context.Context, *connect_go.Request[v15.ReloadSchemaShardRequest]) (*connect_go.Response[v15.ReloadSchemaShardResponse], error)
	// RemoveKeyspaceCell removes the cell from the Cells list for all shards in the keyspace, and the SrvKeyspace for that keyspace in that cell.
	RemoveKeyspaceCell(context.Context, *connect_go.Request[v15.RemoveKeyspaceCellRequest]) (*connect_go.Response[v15.RemoveKeyspaceCellResponse], error)
	// RunHealthCheck runs a healthcheck on the tablet.
	RunHealthCheck(context.Context, *connect_go.Request[v15.RunHealthCheckRequest]) (*connect_go.Response[v15.RunHealthCheckResponse], error)
	// SetReadOnly sets the tablet to read-only mode.
	SetReadOnly(context.Context, *connect_go.Request[v15.SetReadOnlyRequest]) (*connect_go.Response[v15.SetReadOnlyResponse], error)
	// SetReadWrite sets the tablet to read-write mode.
	SetReadWrite(context.Context, *connect_go.Request[v15.SetReadWriteRequest]) (*connect_go.Response[v15.SetReadWriteResponse], error)
	// StartReplication runs the underlying database command to start
	// replication on a tablet.
	StartReplication(context.Context, *connect_go.Request[v15.StartReplicationRequest]) (*connect_go.Response[v15.StartReplicationResponse], error)
	// StopReplication runs the underlying database command to stop replication
	// on a tablet
	StopReplication(context.Context, *connect_go.Request[v15.StopReplicationRequest]) (*connect_go.Response[v15.StopReplicationResponse], error)
	// TabletExternallyPromoted updates the metadata in a cluster's topology
	// to acknowledge a shard primary change performed by an external tool
	// (e.g. orchestrator*).
	//
	// See the Reparenting guide for more information:
	// https://vitess.io/docs/user-guides/configuration-advanced/reparenting/#external-reparenting.
	//
	// * "orchestrator" here refers to external orchestrator, not the newer,
	// Vitess-aware orchestrator, VTOrc.
	TabletExternallyPromoted(context.Context, *connect_go.Request[v15.TabletExternallyPromotedRequest]) (*connect_go.Response[v15.TabletExternallyPromotedResponse], error)
	// Validate validates all nodes in a cluster that are reachable from the global replication graph,
	// as well as all tablets in discoverable cells, are consistent
	Validate(context.Context, *connect_go.Request[v15.ValidateRequest]) (*connect_go.Response[v151.ValidateResponse], error)
	// ValidateKeyspace validates that all nodes reachable from the specified
	// keyspace are consistent.
	ValidateKeyspace(context.Context, *connect_go.Request[v15.ValidateKeyspaceRequest]) (*connect_go.Response[v151.ValidateKeyspaceResponse], error)
	// ValidateSchemaKeyspace validates that the schema on the primary tablet
	// for shard 0 matches the schema on all of the other tablets in the
	// keyspace.
	ValidateSchemaKeyspace(context.Context, *connect_go.Request[v15.ValidateSchemaKeyspaceRequest]) (*connect_go.Response[v151.ValidateSchemaKeyspaceResponse], error)
	// ValidateShard validates that that all nodes reachable from the specified shard are consistent.
	ValidateShard(context.Context, *connect_go.Request[v15.ValidateShardRequest]) (*connect_go.Response[v151.ValidateShardResponse], error)
	// ValidateVersionKeyspace validates that the version on the primary of
	// shard 0 matches all of the other tablets in the keyspace.
	ValidateVersionKeyspace(context.Context, *connect_go.Request[v15.ValidateVersionKeyspaceRequest]) (*connect_go.Response[v151.ValidateVersionKeyspaceResponse], error)
	// ValidateVersionShard validates that the version on the primary matches all of the replicas.
	ValidateVersionShard(context.Context, *connect_go.Request[v15.ValidateVersionShardRequest]) (*connect_go.Response[v151.ValidateVersionShardResponse], error)
	// VTExplain provides information on how Vitess plans to execute a
	// particular query.
	VTExplain(context.Context, *connect_go.Request[v15.VTExplainRequest]) (*connect_go.Response[v15.VTExplainResponse], error)
}

