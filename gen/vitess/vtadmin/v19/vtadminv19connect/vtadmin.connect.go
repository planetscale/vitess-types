// VTAdminHandler is an implementation of the vtadmin.VTAdmin service.
type VTAdminHandler interface {
	// ApplySchema applies a schema to a keyspace in the given cluster.
	ApplySchema(context.Context, *connect.Request[v19.ApplySchemaRequest]) (*connect.Response[v191.ApplySchemaResponse], error)
	// CancelSchemaMigration cancels one or all schema migrations in the given
	// cluster, terminating any running ones as needed.
	CancelSchemaMigration(context.Context, *connect.Request[v19.CancelSchemaMigrationRequest]) (*connect.Response[v191.CancelSchemaMigrationResponse], error)
	// CleanupSchemaMigration marks a schema migration in the given cluster as
	// ready for artifact cleanup.
	CleanupSchemaMigration(context.Context, *connect.Request[v19.CleanupSchemaMigrationRequest]) (*connect.Response[v191.CleanupSchemaMigrationResponse], error)
	// CompleteSchemaMigration completes one or all migrations in the given
	// cluster executed with --postpone-completion.
	CompleteSchemaMigration(context.Context, *connect.Request[v19.CompleteSchemaMigrationRequest]) (*connect.Response[v191.CompleteSchemaMigrationResponse], error)
	// CreateKeyspace creates a new keyspace in the given cluster.
	CreateKeyspace(context.Context, *connect.Request[v19.CreateKeyspaceRequest]) (*connect.Response[v19.CreateKeyspaceResponse], error)
	// CreateShard creates a new shard in the given cluster and keyspace.
	CreateShard(context.Context, *connect.Request[v19.CreateShardRequest]) (*connect.Response[v191.CreateShardResponse], error)
	// DeleteKeyspace deletes a keyspace in the given cluster.
	DeleteKeyspace(context.Context, *connect.Request[v19.DeleteKeyspaceRequest]) (*connect.Response[v191.DeleteKeyspaceResponse], error)
	// DeleteShard deletes one or more shards in the given cluster and keyspace.
	DeleteShards(context.Context, *connect.Request[v19.DeleteShardsRequest]) (*connect.Response[v191.DeleteShardsResponse], error)
	// DeleteTablet deletes a tablet from the topology
	DeleteTablet(context.Context, *connect.Request[v19.DeleteTabletRequest]) (*connect.Response[v19.DeleteTabletResponse], error)
	// EmergencyFailoverShard fails over a shard to a new primary. It assumes
	// the old primary is dead or otherwise not responding.
	EmergencyFailoverShard(context.Context, *connect.Request[v19.EmergencyFailoverShardRequest]) (*connect.Response[v19.EmergencyFailoverShardResponse], error)
	// FindSchema returns a single Schema that matches the provided table name
	// across all specified clusters IDs. Not specifying a set of cluster IDs
	// causes the search to span all configured clusters.
	//
	// An error occurs if either no table exists across any of the clusters with
	// the specified table name, or if multiple tables exist with that name.
	FindSchema(context.Context, *connect.Request[v19.FindSchemaRequest]) (*connect.Response[v19.Schema], error)
	// GetBackups returns backups grouped by cluster.
	GetBackups(context.Context, *connect.Request[v19.GetBackupsRequest]) (*connect.Response[v19.GetBackupsResponse], error)
	// GetCellInfos returns the CellInfo objects for the specified clusters.
	//
	// Callers may optionally restrict the set of CellInfos, or restrict the
	// response to include only cell names.
	GetCellInfos(context.Context, *connect.Request[v19.GetCellInfosRequest]) (*connect.Response[v19.GetCellInfosResponse], error)
	// GetCellsAliases returns the CellsAliases data for the specified clusters.
	GetCellsAliases(context.Context, *connect.Request[v19.GetCellsAliasesRequest]) (*connect.Response[v19.GetCellsAliasesResponse], error)
	// GetClusters returns all configured clusters.
	GetClusters(context.Context, *connect.Request[v19.GetClustersRequest]) (*connect.Response[v19.GetClustersResponse], error)
	// GetFullStatus returns the full status of MySQL including the replication information, semi-sync information, GTID information among others
	GetFullStatus(context.Context, *connect.Request[v19.GetFullStatusRequest]) (*connect.Response[v191.GetFullStatusResponse], error)
	// GetGates returns all gates across all the specified clusters.
	GetGates(context.Context, *connect.Request[v19.GetGatesRequest]) (*connect.Response[v19.GetGatesResponse], error)
	// GetKeyspace returns a keyspace by name in the specified cluster.
	GetKeyspace(context.Context, *connect.Request[v19.GetKeyspaceRequest]) (*connect.Response[v19.Keyspace], error)
	// GetKeyspaces returns all keyspaces across the specified clusters.
	GetKeyspaces(context.Context, *connect.Request[v19.GetKeyspacesRequest]) (*connect.Response[v19.GetKeyspacesResponse], error)
	// GetSchema returns the schema for the specified (cluster, keyspace, table)
	// tuple.
	GetSchema(context.Context, *connect.Request[v19.GetSchemaRequest]) (*connect.Response[v19.Schema], error)
	// GetSchemas returns all schemas across the specified clusters.
	GetSchemas(context.Context, *connect.Request[v19.GetSchemasRequest]) (*connect.Response[v19.GetSchemasResponse], error)
	// GetSchemaMigrations returns one or more online schema migrations for the
	// set of keyspaces (or all keyspaces) in the given clusters, analagous to
	// repeated executions of `SHOW VITESS_MIGRATIONS`.
	//
	// Different fields in the request message result in different behaviors.
	// See the documentation on vtctldata.GetSchemaMigrationsRequest for details.
	GetSchemaMigrations(context.Context, *connect.Request[v19.GetSchemaMigrationsRequest]) (*connect.Response[v19.GetSchemaMigrationsResponse], error)
	// GetShardReplicationPositions returns shard replication positions grouped
	// by cluster.
	GetShardReplicationPositions(context.Context, *connect.Request[v19.GetShardReplicationPositionsRequest]) (*connect.Response[v19.GetShardReplicationPositionsResponse], error)
	// GetSrvKeyspace returns the SrvKeyspace for a keyspace in one or more cells.
	GetSrvKeyspace(context.Context, *connect.Request[v19.GetSrvKeyspaceRequest]) (*connect.Response[v191.GetSrvKeyspacesResponse], error)
	// GetSrvKeyspaces returns the SrvKeyspaces for all keyspaces across all the specified clusters.
	GetSrvKeyspaces(context.Context, *connect.Request[v19.GetSrvKeyspacesRequest]) (*connect.Response[v19.GetSrvKeyspacesResponse], error)
	// GetSrvVSchema returns the SrvVSchema for the given cluster and cell.
	GetSrvVSchema(context.Context, *connect.Request[v19.GetSrvVSchemaRequest]) (*connect.Response[v19.SrvVSchema], error)
	// GetSrvVSchemas returns all SrvVSchemas across all (or specified) clusters
	// and cells.
	GetSrvVSchemas(context.Context, *connect.Request[v19.GetSrvVSchemasRequest]) (*connect.Response[v19.GetSrvVSchemasResponse], error)
	// GetTablet looks up a tablet by hostname across all clusters and returns
	// the result.
	GetTablet(context.Context, *connect.Request[v19.GetTabletRequest]) (*connect.Response[v19.Tablet], error)
	// GetTablets returns all tablets across all the specified clusters.
	GetTablets(context.Context, *connect.Request[v19.GetTabletsRequest]) (*connect.Response[v19.GetTabletsResponse], error)
	// GetTopologyPath returns the cell located at the specified path in the topology server.
	GetTopologyPath(context.Context, *connect.Request[v19.GetTopologyPathRequest]) (*connect.Response[v191.GetTopologyPathResponse], error)
	// GetVSchema returns a VSchema for the specified keyspace in the specified
	// cluster.
	GetVSchema(context.Context, *connect.Request[v19.GetVSchemaRequest]) (*connect.Response[v19.VSchema], error)
	// GetVSchemas returns the VSchemas for all specified clusters.
	GetVSchemas(context.Context, *connect.Request[v19.GetVSchemasRequest]) (*connect.Response[v19.GetVSchemasResponse], error)
	// GetVtctlds returns the Vtctlds for all specified clusters.
	GetVtctlds(context.Context, *connect.Request[v19.GetVtctldsRequest]) (*connect.Response[v19.GetVtctldsResponse], error)
	// GetWorkflow returns a single Workflow for a given cluster, keyspace, and
	// workflow name.
	GetWorkflow(context.Context, *connect.Request[v19.GetWorkflowRequest]) (*connect.Response[v19.Workflow], error)
	// GetWorkflows returns the Workflows for all specified clusters.
	GetWorkflows(context.Context, *connect.Request[v19.GetWorkflowsRequest]) (*connect.Response[v19.GetWorkflowsResponse], error)
	// LaunchSchemaMigration launches one or all migrations in the given
	// cluster executed with --postpone-launch.
	LaunchSchemaMigration(context.Context, *connect.Request[v19.LaunchSchemaMigrationRequest]) (*connect.Response[v191.LaunchSchemaMigrationResponse], error)
	// PingTablet checks that the specified tablet is awake and responding to
	// RPCs. This command can be blocked by other in-flight operations.
	PingTablet(context.Context, *connect.Request[v19.PingTabletRequest]) (*connect.Response[v19.PingTabletResponse], error)
	// PlannedFailoverShard fails over the shard to a new primary, or away from
	// an old primary. Both the old and new primaries must be reachable and
	// running.
	//
	// NOTE: A planned failover will not consider replicas outside the current
	// shard primary's cell as promotion candidates unless NewPrimary is
	// explicitly provided in the request.
	PlannedFailoverShard(context.Context, *connect.Request[v19.PlannedFailoverShardRequest]) (*connect.Response[v19.PlannedFailoverShardResponse], error)
	// RebuildKeyspaceGraph rebuilds the serving data for a keyspace.
	RebuildKeyspaceGraph(context.Context, *connect.Request[v19.RebuildKeyspaceGraphRequest]) (*connect.Response[v19.RebuildKeyspaceGraphResponse], error)
	// RefreshState reloads the tablet record on the specified tablet.
	RefreshState(context.Context, *connect.Request[v19.RefreshStateRequest]) (*connect.Response[v19.RefreshStateResponse], error)
	// RefreshTabletReplicationSource performs a `CHANGE REPLICATION SOURCE TO`
	// on a tablet to replicate from the current primary in the shard.
	RefreshTabletReplicationSource(context.Context, *connect.Request[v19.RefreshTabletReplicationSourceRequest]) (*connect.Response[v19.RefreshTabletReplicationSourceResponse], error)
	// ReloadSchemas reloads the schema definition across keyspaces, shards, or
	// tablets in one or more clusters, depending on the request fields (see
	// ReloadSchemasRequest for details).
	ReloadSchemas(context.Context, *connect.Request[v19.ReloadSchemasRequest]) (*connect.Response[v19.ReloadSchemasResponse], error)
	// ReloadSchemaShard reloads the schema on all tablets in a shard. This is done on a best-effort basis.
	ReloadSchemaShard(context.Context, *connect.Request[v19.ReloadSchemaShardRequest]) (*connect.Response[v19.ReloadSchemaShardResponse], error)
	// RemoveKeyspaceCell removes the cell from the Cells list for all shards in the keyspace, and the SrvKeyspace for that keyspace in that cell.
	RemoveKeyspaceCell(context.Context, *connect.Request[v19.RemoveKeyspaceCellRequest]) (*connect.Response[v19.RemoveKeyspaceCellResponse], error)
	// RetrySchemaMigration marks a given schema migration in the given cluster
	// for retry.
	RetrySchemaMigration(context.Context, *connect.Request[v19.RetrySchemaMigrationRequest]) (*connect.Response[v191.RetrySchemaMigrationResponse], error)
	// RunHealthCheck runs a healthcheck on the tablet.
	RunHealthCheck(context.Context, *connect.Request[v19.RunHealthCheckRequest]) (*connect.Response[v19.RunHealthCheckResponse], error)
	// SetReadOnly sets the tablet to read-only mode.
	SetReadOnly(context.Context, *connect.Request[v19.SetReadOnlyRequest]) (*connect.Response[v19.SetReadOnlyResponse], error)
	// SetReadWrite sets the tablet to read-write mode.
	SetReadWrite(context.Context, *connect.Request[v19.SetReadWriteRequest]) (*connect.Response[v19.SetReadWriteResponse], error)
	// StartReplication runs the underlying database command to start
	// replication on a tablet.
	StartReplication(context.Context, *connect.Request[v19.StartReplicationRequest]) (*connect.Response[v19.StartReplicationResponse], error)
	// StopReplication runs the underlying database command to stop replication
	// on a tablet
	StopReplication(context.Context, *connect.Request[v19.StopReplicationRequest]) (*connect.Response[v19.StopReplicationResponse], error)
	// TabletExternallyPromoted updates the metadata in a cluster's topology
	// to acknowledge a shard primary change performed by an external tool
	// (e.g. orchestrator*).
	//
	// See the Reparenting guide for more information:
	// https://vitess.io/docs/user-guides/configuration-advanced/reparenting/#external-reparenting.
	//
	// * "orchestrator" here refers to external orchestrator, not the newer,
	// Vitess-aware orchestrator, VTOrc.
	TabletExternallyPromoted(context.Context, *connect.Request[v19.TabletExternallyPromotedRequest]) (*connect.Response[v19.TabletExternallyPromotedResponse], error)
	// Validate validates all nodes in a cluster that are reachable from the global replication graph,
	// as well as all tablets in discoverable cells, are consistent
	Validate(context.Context, *connect.Request[v19.ValidateRequest]) (*connect.Response[v191.ValidateResponse], error)
	// ValidateKeyspace validates that all nodes reachable from the specified
	// keyspace are consistent.
	ValidateKeyspace(context.Context, *connect.Request[v19.ValidateKeyspaceRequest]) (*connect.Response[v191.ValidateKeyspaceResponse], error)
	// ValidateSchemaKeyspace validates that the schema on the primary tablet
	// for shard 0 matches the schema on all of the other tablets in the
	// keyspace.
	ValidateSchemaKeyspace(context.Context, *connect.Request[v19.ValidateSchemaKeyspaceRequest]) (*connect.Response[v191.ValidateSchemaKeyspaceResponse], error)
	// ValidateShard validates that that all nodes reachable from the specified shard are consistent.
	ValidateShard(context.Context, *connect.Request[v19.ValidateShardRequest]) (*connect.Response[v191.ValidateShardResponse], error)
	// ValidateVersionKeyspace validates that the version on the primary of
	// shard 0 matches all of the other tablets in the keyspace.
	ValidateVersionKeyspace(context.Context, *connect.Request[v19.ValidateVersionKeyspaceRequest]) (*connect.Response[v191.ValidateVersionKeyspaceResponse], error)
	// ValidateVersionShard validates that the version on the primary matches all of the replicas.
	ValidateVersionShard(context.Context, *connect.Request[v19.ValidateVersionShardRequest]) (*connect.Response[v191.ValidateVersionShardResponse], error)
	// VTExplain provides information on how Vitess plans to execute a
	// particular query.
	VTExplain(context.Context, *connect.Request[v19.VTExplainRequest]) (*connect.Response[v19.VTExplainResponse], error)
}

